package util;

/**
 *
 * @author claudia
 */
public class IDEmail {

    private String id = null;
    private String email = null;

    public IDEmail(String id, String email) {
        this.id = id;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }
}
