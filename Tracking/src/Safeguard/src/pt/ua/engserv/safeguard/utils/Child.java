package pt.ua.engserv.safeguard.utils;

public class Child {
	
	private int id;
	private String name;
	private String photo;
	
	public Child(int id, String name, String photo) {
		
		this.id = id;
		this.name = name;
		this.photo = photo;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getPhoto() {
		return photo;
	}
	
	

}
